        st.markdown('<div class="section-title">⚡ High-Fidelity Predictive Intelligence</div>', unsafe_allow_html=True)
        st.caption("Microsecond inference generated by the serialized Random Forest Engine pipeline.")
        
        input_features = pd.DataFrame({
            "Time_Hours": [time_hours],
            "Temperature": [temperature],
            "pH": [ph],
            "Dissolved_Oxygen": [dissolved_oxygen],
            "Glucose_Feed_Rate": [glucose_feed]
        })

        predicted_yield = pipeline.predict(input_features)[0]

