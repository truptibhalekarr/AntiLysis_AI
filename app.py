            with kpi_col1:
                st.metric(
                    label="PREDICTED BIOMASS YIELD", 
                    value=f"{predicted_yield:.2f} g/L",
                    delta=f"{predicted_yield - 12.0:.1f} g/L from start" if predicted_yield > 12.0 else None
                )
                
            with kpi_col2:
                status_label = "🚨 CRITICAL LYSIS RISK" if anomalies_active else "✅ HEALTHY REPLICATION"
                st.metric(label="REACTOR OPERATIONAL STATE", value=status_label)
                
            with kpi_col3:
                efficiency = "Optimal" if not anomalies_active else "Degraded"
                st.metric(label="METABOLIC EFFICIENCY", value=efficiency)

